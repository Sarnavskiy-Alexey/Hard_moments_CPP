/*
1. Реализуйте шаблонную функцию Swap, которая принимает два указателя и обменивает местами
   значения, на которые указывают эти указатели (нужно обменивать именно сами указатели,
   переменные должны оставаться в тех же адресах памяти).
*/

#pragma once

#include <iostream>

template <typename T>
void Swap(T*& par_a, T*& par_b)
{
    T* tmp = par_a;
    par_a = par_b;
    par_b = tmp;
}
